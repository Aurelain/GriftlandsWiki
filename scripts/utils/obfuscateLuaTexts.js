const toBase64 = require('./toBase64');

/**
 *
 */
const obfuscateLuaTexts = (lua) => {
    return lua.replace(/"([^"]*)"/g, obfuscateTexts);
};

// =====================================================================================================================
//  P R I V A T E
// =====================================================================================================================
/**
 *
 */
const obfuscateTexts = (found, inner) => {
    return '"#' + toBase64(inner) + '"';
};

// =====================================================================================================================
//  E X P O R T
// =====================================================================================================================
module.exports = obfuscateLuaTexts;
